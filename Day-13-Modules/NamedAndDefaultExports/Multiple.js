
//create module that exports multiple functions using named exports. importand use these functions in another script

export const add = (a, b) => {
    return a + b;
}

export const subtract = (a, b) => {
    return a - b;
}

export const multiply = (a, b) => {
    return a * b;
}

export const divide = (a, b) => {
    return a / b;
}